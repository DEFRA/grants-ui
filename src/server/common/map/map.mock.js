/** @returns {{ minLng: number, minLat: number, maxLng: number, maxLat: number }} */
function mockBbox() {
  let minLng = Infinity,
    minLat = Infinity,
    maxLng = -Infinity,
    maxLat = -Infinity
  for (const geom of MOCK_GEOMETRIES) {
    for (const ring of geom.coordinates) {
      for (const [lng, lat] of ring) {
        if (lng < minLng) {
          minLng = lng
        }
        if (lat < minLat) {
          minLat = lat
        }
        if (lng > maxLng) {
          maxLng = lng
        }
        if (lat > maxLat) {
          maxLat = lat
        }
      }
    }
  }
  return { minLng, minLat, maxLng, maxLat }
}

